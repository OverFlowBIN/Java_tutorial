
public class Variable {

	public static void main(String[] args) {
		// Variable: 그 값이 변할수 있는 문자
		// Java는 변수를 설정할 때 Data Type을 지정해줘야 한다.
			// 값을 넣을때는 까다롭지만 지정된 변수의 값의 타입을 확신할 수 있다.
		
		// int(정수, natural number) 
		int a = 1;
		System.out.println(a);
		
		// double(실수, real number)
		double b = 1.1;
		System.out.println(b);
		
		// String (문자열, string)
		String c = "Hello World";
		System.out.println(c);

		
	}

}
